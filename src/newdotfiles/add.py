"""Example module with basic arithmetic operations."""


def add(x: int | float, y: int | float) -> int | float:
    """Add two numbers together.

    Args:
        x: First number
        y: Second number

    Returns:
        Sum of x and y
    """
    return x + y


def multiply(x: int | float, y: int | float) -> int | float:
    """Multiply two numbers together.

    Args:
        x: First number
        y: Second number

    Returns:
        Product of x and y
    """
    return x * y
